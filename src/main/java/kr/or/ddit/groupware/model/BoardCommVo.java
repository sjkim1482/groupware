package kr.or.ddit.groupware.model;

import java.util.Date;

// 게시판 댓글
public class BoardCommVo {
	
	private int com_no;
	private Date reg_dt;
	private String cont;
	private int del_cd;
	private int post_no;
	private int emp_no;
	private String ko_nm;
	
	public BoardCommVo() {}

	public int getCom_no() {
		return com_no;
	}

	public void setCom_no(int com_no) {
		this.com_no = com_no;
	}

	public Date getReg_dt() {
		return reg_dt;
	}

	public void setReg_dt(Date reg_dt) {
		this.reg_dt = reg_dt;
	}

	public String getCont() {
		return cont;
	}

	public void setCont(String cont) {
		this.cont = cont;
	}

	public int getDel_cd() {
		return del_cd;
	}

	public void setDel_cd(int del_cd) {
		this.del_cd = del_cd;
	}

	public int getPost_no() {
		return post_no;
	}

	public void setPost_no(int post_no) {
		this.post_no = post_no;
	}

	public int getEmp_no() {
		return emp_no;
	}

	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}

	public String getKo_nm() {
		return ko_nm;
	}

	public void setKo_nm(String ko_nm) {
		this.ko_nm = ko_nm;
	}

	@Override
	public String toString() {
		return "BoardCommVo [com_no=" + com_no + ", reg_dt=" + reg_dt + ", cont=" + cont + ", del_cd=" + del_cd
				+ ", post_no=" + post_no + ", emp_no=" + emp_no + ", ko_nm=" + ko_nm + "]";
	}

	public BoardCommVo(int com_no, Date reg_dt, String cont, int del_cd, int post_no, int emp_no, String ko_nm) {
		super();
		this.com_no = com_no;
		this.reg_dt = reg_dt;
		this.cont = cont;
		this.del_cd = del_cd;
		this.post_no = post_no;
		this.emp_no = emp_no;
		this.ko_nm = ko_nm;
	}

	
	
	
	

	
	
	

}
