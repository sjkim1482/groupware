package kr.or.ddit.groupware.model;

// 게시판
public class BoardVo {

	
	private int bor_no;
	private String bor_nm;
	private int emp_no;
	private int act_cd;
	
	public BoardVo() {}

	public int getBor_no() {
		return bor_no;
	}

	public void setBor_no(int bor_no) {
		this.bor_no = bor_no;
	}

	public String getBor_nm() {
		return bor_nm;
	}

	public void setBor_nm(String bor_nm) {
		this.bor_nm = bor_nm;
	}

	public int getEmp_no() {
		return emp_no;
	}

	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}

	public int getAct_cd() {
		return act_cd;
	}

	public void setAct_cd(int act_cd) {
		this.act_cd = act_cd;
	}

	public BoardVo(int bor_no, String bor_nm, int emp_no, int act_cd) {
		super();
		this.bor_no = bor_no;
		this.bor_nm = bor_nm;
		this.emp_no = emp_no;
		this.act_cd = act_cd;
	}

	@Override
	public String toString() {
		return "BoardVo [bor_no=" + bor_no + ", bor_nm=" + bor_nm + ", emp_no=" + emp_no + ", act_cd=" + act_cd + "]";
	}
	
	
	
}
