package kr.or.ddit.groupware.model;

import java.util.Date;
import java.util.List;

public class MeetingVo {

	private int meet_no;
	private String meet_nm;
	private Date s_time;
	private Date e_time;
	private int emp_no;
	private int sta_cd;
	private List<Integer> empList;
	private String ko_nm; 
	private String cd_ko_nm;
	
	
	public String getKo_nm() {
		return ko_nm;
	}
	public void setKo_nm(String ko_nm) {
		this.ko_nm = ko_nm;
	}
	public String getCd_ko_nm() {
		return cd_ko_nm;
	}
	public void setCd_ko_nm(String cd_ko_nm) {
		this.cd_ko_nm = cd_ko_nm;
	}
	public List<Integer> getEmpList() {
		return empList;
	}
	public void setEmpList(List<Integer> empList) {
		this.empList = empList;
	}
	public int getMeet_no() {
		return meet_no;
	}
	public void setMeet_no(int meet_no) {
		this.meet_no = meet_no;
	}
	public String getMeet_nm() {
		return meet_nm;
	}
	public void setMeet_nm(String meet_nm) {
		this.meet_nm = meet_nm;
	}
	public Date getS_time() {
		return s_time;
	}
	public void setS_time(Date s_time) {
		this.s_time = s_time;
	}
	public Date getE_time() {
		return e_time;
	}
	public void setE_time(Date e_time) {
		this.e_time = e_time;
	}
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	public int getSta_cd() {
		return sta_cd;
	}
	public void setSta_cd(int sta_cd) {
		this.sta_cd = sta_cd;
	}
	@Override
	public String toString() {
		return "MeetingVo [meet_no=" + meet_no + ", meet_nm=" + meet_nm + ", s_time=" + s_time + ", e_time=" + e_time
				+ ", emp_no=" + emp_no + ", sta_cd=" + sta_cd + ", empList=" + empList + ", ko_nm=" + ko_nm
				+ ", cd_ko_nm=" + cd_ko_nm + "]";
	}
	

	
	
	
}
