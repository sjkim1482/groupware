package kr.or.ddit.groupware.model;

// 게시판 게시글 첨부 파일
public class BoardPostFileVo {
	
	private int file_no;
	private int post_no;
	
	public BoardPostFileVo() {}

	public int getFile_no() {
		return file_no;
	}

	public void setFile_no(int file_no) {
		this.file_no = file_no;
	}

	public int getPost_no() {
		return post_no;
	}

	public void setPost_no(int post_no) {
		this.post_no = post_no;
	}

	public BoardPostFileVo(int file_no, int post_no) {
		super();
		this.file_no = file_no;
		this.post_no = post_no;
	}

	@Override
	public String toString() {
		return "BoardPostFileVo [file_no=" + file_no + ", post_no=" + post_no + "]";
	}
	
	
	
	
}
