package kr.or.ddit.groupware.approval.model;

import java.util.List;

public class PerAppLineInfoVo {
	
	private int per_app_line_no;
	private String per_app_line_nm;
	private int emp_no         ;
	private List<Integer> empList;
	public int getPer_app_line_no() {
		return per_app_line_no;
	}
	public void setPer_app_line_no(int per_app_line_no) {
		this.per_app_line_no = per_app_line_no;
	}
	public String getPer_app_line_nm() {
		return per_app_line_nm;
	}
	public void setPer_app_line_nm(String per_app_line_nm) {
		this.per_app_line_nm = per_app_line_nm;
	}
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	public List<Integer> getEmpList() {
		return empList;
	}
	public void setEmpList(List<Integer> empList) {
		this.empList = empList;
	}
	@Override
	public String toString() {
		return "PerAppLineInfoVo [per_app_line_no=" + per_app_line_no + ", per_app_line_nm=" + per_app_line_nm
				+ ", emp_no=" + emp_no + ", empList=" + empList + "]";
	}
	
	
	
	
}
