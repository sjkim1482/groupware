package kr.or.ddit.groupware.approval.model;

public class AppDetDivVo {
	
	private int app_det_no  ;
	private int app_div_no  ;
	private String app_det_cont;
	
	public int getApp_det_no() {
		return app_det_no;
	}
	public void setApp_det_no(int app_det_no) {
		this.app_det_no = app_det_no;
	}
	public int getApp_div_no() {
		return app_div_no;
	}
	public void setApp_div_no(int app_div_no) {
		this.app_div_no = app_div_no;
	}
	public String getApp_det_cont() {
		return app_det_cont;
	}
	public void setApp_det_cont(String app_det_cont) {
		this.app_det_cont = app_det_cont;
	}
	@Override
	public String toString() {
		return "AppDetDivVo [app_det_no=" + app_det_no + ", app_div_no=" + app_div_no + ", app_det_cont=" + app_det_cont
				+ "]";
	}
	
	
	
}
